# Determine actual separation of encoding vs roleplay
t01_t03_separated = False
if 'technique_type' in sample.columns:
    for enc_tech in ['T03', 'T05']:
        mask_enc = (sample['technique_type'] == enc_tech).values
        mask_t01 = (sample['technique_type'] == 'T01').values
        if mask_enc.sum() > 5 and mask_t01.sum() > 5:
            c_enc = coords[mask_enc].mean(axis=0)
            c_t01 = coords[mask_t01].mean(axis=0)
            dist  = np.linalg.norm(c_enc - c_t01)
            spread = ((np.std(coords[mask_enc], axis=0).mean() +
                       np.std(coords[mask_t01], axis=0).mean()) / 2)
            if dist > spread * 1.5:
                t01_t03_separated = True
                enc_label = enc_tech
                enc_dist  = dist
                break

