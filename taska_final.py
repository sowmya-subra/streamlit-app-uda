def safe_violin(ax, datasets, positions, colors):
    """Violin with fallback to boxplot for tiny/degenerate groups."""
    clean, valid_pos, valid_colors = [], [], []
    for d, p, c in zip(datasets, positions, colors):
        arr = np.array(d)
