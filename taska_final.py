    """Lemmatize with spaCy, remove stops, keep 4+ char alpha tokens."""
    results = []
    for doc in _nlp_lda.pipe(
            [str(t).lower()[:512] for t in texts],
            batch_size=batch_size):
        tokens = [
            token.lemma_
            for token in doc
            if  len(token.lemma_) >= 4
            and token.lemma_.isalpha()
            and not token.is_stop
            and token.lemma_ not in ALL_STOPS
            and token.pos_ not in {'ADJ', 'ADV'}  # block adjectives/adverbs at POS level
        ]
        results.append(' '.join(tokens))
    return results

