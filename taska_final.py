def compute_sil(coords, labels_series):
    try:
        le     = LabelEncoder()
        labels = le.fit_transform(labels_series.fillna('unknown'))
        if len(set(labels)) < 2:
            return None, None, None
        overall = round(silhouette_score(
            coords, labels, metric='euclidean',
            sample_size=min(1000, len(coords)),
            random_state=RANDOM_SEED), 4)
        # Per-class silhouette
        sample_scores = silhouette_samples(coords, labels, metric='euclidean')
        per_class = {}
        for cls_idx, cls_name in enumerate(le.classes_):
            mask = labels == cls_idx
            per_class[cls_name] = round(float(sample_scores[mask].mean()), 4)
        return overall, per_class, le
    except Exception as e:
        print(f"  Silhouette error: {e}")
        return None, None, None

