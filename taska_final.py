def coherence_proxy(lda_model, dtm_matrix, top_n=10):
    """
    Proxy coherence: mean pairwise PMI-like co-occurrence score.
    Uses top_n words per topic. Higher = more coherent topics.
    """
    doc_freq = np.asarray(dtm_matrix.sum(axis=0)).flatten()
    n_docs = dtm_matrix.shape[0]
    scores = []
    for comp in lda_model.components_:
        top_idx = comp.argsort()[-top_n:]
        for i in range(len(top_idx)):
            for j in range(i+1, len(top_idx)):
                wi, wj = top_idx[i], top_idx[j]
                # co-occurrence via column dot product (sparse-friendly)
                col_i = dtm_matrix.getcol(wi).toarray().flatten() > 0
                col_j = dtm_matrix.getcol(wj).toarray().flatten() > 0
                co = (col_i & col_j).sum()
                if co > 0 and doc_freq[wi] > 0 and doc_freq[wj] > 0:
                    pmi = np.log((co * n_docs) /
                                 (doc_freq[wi] * doc_freq[wj]) + 1e-9)
                    scores.append(pmi)
    return float(np.mean(scores)) if scores else 0.0

