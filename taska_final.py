def compute_lift(col, params, df_s, cat_key):
    if col not in df_s.columns:
        return [], 0, 0
    high_mask = df_s[col] > params['high']
    low_mask  = df_s[col] < params['low']
    n_high, n_low = high_mask.sum(), low_mask.sum()
    if n_high < 20 or n_low < 100:
        return [], n_high, n_low
    high_tokens = df_s.loc[high_mask, '_tokens'].tolist()
    low_tokens  = df_s.loc[low_mask,  '_tokens'].tolist()
    vocab = set().union(*high_tokens) if high_tokens else set()

    low_doc_freq = {}
    for tok_set in low_tokens:
        for w in tok_set:
            low_doc_freq[w] = low_doc_freq.get(w, 0) + 1

    records = []
    for w in vocab:
        doc_high = sum(1 for s in high_tokens if w in s)
        doc_low  = low_doc_freq.get(w, 0)
        if doc_high < params['min_doc_high'] or doc_low < params['min_doc_low']:
            continue
        freq_high = doc_high / n_high
        freq_low  = doc_low  / n_low
        if freq_high < MIN_FREQ_HIGH_FLOOR:
            continue
        smooth_low = freq_low + (1.0 / n_low)
        raw_lift = freq_high / smooth_low
        lift     = min(raw_lift, LIFT_CAP)
        if lift < params['min_lift']:
            continue
        if not is_domain_relevant(w, cat_key):
            continue
        records.append({
            'word': w, 'category': cat_key,
            'freq_high': round(freq_high, 6), 'freq_low': round(freq_low, 6),
            'lift': round(lift, 4), 'raw_lift': round(raw_lift, 4),
            'doc_high': doc_high, 'doc_low': doc_low,
            'n_high': n_high, 'n_low': n_low,
        })
    return records, n_high, n_low

