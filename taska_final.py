# Flatten all domain seeds into one lookup
ALL_DOMAIN_WORDS = set()
for seeds in DOMAIN_SEEDS.values():
    ALL_DOMAIN_WORDS.update(seeds)

def is_domain_relevant(word, category):
    """Check if word matches domain seeds for this category."""
    # Direct match
    if word in DOMAIN_SEEDS.get(category, set()):
        return True
    # Prefix match (4+ chars)
    for seed in DOMAIN_SEEDS.get(category, set()):
        if len(seed) >= 4 and (word.startswith(seed[:4]) or seed.startswith(word[:4])):
            return True
    return False

def is_domain_word_globally(word):
    """Check if word appears in ANY domain seed (global check)."""
    if word in ALL_DOMAIN_WORDS:
        return True
    # Prefix match against any seed
    for seed in ALL_DOMAIN_WORDS:
        if len(seed) >= 4 and len(word) >= 4:
            if word.startswith(seed[:4]) or seed.startswith(word[:4]):
                return True
    return False

def pos_tokenize_batch(texts, batch_size=64):
    """
    Token selection (order is critical):
    1. Skip punctuation / short / non-alpha — always.
    2. Domain seed words bypass stopword filter — always kept.
       REASON: seeds like 'hate','harm','abuse','death','pain' are in
       STOPWORDS but are the primary discriminative signals for lift.
       Without this bypass they are silently dropped and categories
       produce zero qualifying words.
    3. Non-seed words: apply stopword filter, keep NOUN/PROPN only.
       Adjectives (ADJ) and adverbs (ADV) are blocked here — they are
       sentiment/description words that dilute lift discrimination.
    """
    results = []
    texts_capped = [str(t)[:512] for t in texts]

    for doc in nlp.pipe(texts_capped, batch_size=batch_size):
        tokens = set()

        for token in doc:
            word = token.text.lower()

            # Step 1: always skip punct / short / non-alpha
            if token.is_punct or len(word) < 4 or not word.isalpha():
                continue

            # Step 2: domain seeds bypass stopword filter
            if is_domain_word_globally(word):
                tokens.add(word)
                continue

            # Step 3: stopword filter + POS gate for non-seed words
            if token.is_stop or word in STOPWORDS:
                continue

            if token.pos_ in {'NOUN', 'PROPN'}:
                tokens.add(word)

        results.append(tokens)

    return results

